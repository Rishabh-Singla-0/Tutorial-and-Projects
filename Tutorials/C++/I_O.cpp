// The bits/stdc++.h is a header that includes a vast number of c++ libraries
// It may cause potential compatibility issues and impact on compile time, especially in large projects
#include<bits/stdc++.h>
using namespace std;

int main() {
    
    // \n to move the cursor to the next line (low-level operation)
    cout << "Hello World\n";

    // endl to get to next line and empty output buffer (high-level operation)
    cout << "My Name is Rishabh Singla" << endl;

    // Taking an integer as an input and assigning it to x and outputing it
    int x;
    cout << "Input a Value: ";
    cin >> x;
    cout << "The Value of x is " << x;

    return 0;
}